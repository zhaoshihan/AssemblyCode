DATA	SEGMENT
COUNT	EQU	100
DATA	ENDS
CODE	SEGMENT
ASSUME	CS:CODE,DS:DATA
START:	MOV AX,DATA
	MOV DS,AX
	SUB AX,AX
	SUB BX,BX
	SUB CX,CX
			;
AGAIN:	INC AL
	SUB DX,DX
	MOV DH,AL	;STORE AL VALUE TO DH
	MOV BL,1	
			;
PRIME:	MOV AH,0	;SET 0 FOR DIV
	DIV BL
	CMP AH,0
	JNZ NEXT
	INC DL
NEXT:	INC BL
	MOV AL,DH	; AH 从余数变为num
	CMP BL,AL
	JLE PRIME	; BL <= AL JUMP
			;
	CMP DL,2	;ISPRIME?
	JNZ NO
PRINT:	MOV AH,0
	MOV BL,10
	DIV BL
	XCHG AH,AL
	ADD AX,3030H
	MOV CX,AX
	MOV DL,CH
	MOV AH,2
	INT 21H
	MOV DL,CL
	MOV AH,2
	INT 21H
	MOV DL,32	;空格显示
	MOV AH,2
	INT 21H
	MOV AL,DH
			;
NO:	CMP AL,COUNT	; AL < COUNT, CONTINUE
	JL AGAIN
			;
	MOV AX,4C00H
	INT 21H
CODE	ENDS
END	START
